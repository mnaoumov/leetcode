using NUnit.Framework;
using JetBrains.Annotations;

namespace LeetCode.Problems._1493_Longest_Subarray_of_1_s_After_Deleting_One_Element;

[UsedImplicitly]
public class Tests : TestsBase<ISolution, Tests.TestCase>
{

using JetBrains.Annotations;
using LeetCode.Base;
using NUnit.Framework;

namespace LeetCode.Problems._2780_Minimum_Index_of_a_Valid_Split;

[UsedImplicitly]
public class Tests : TestsBase<ISolution, Tests.TestCase>
{
